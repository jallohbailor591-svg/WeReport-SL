export const getStatusBadge = (status: string) => {
  const badges: Record<string, { label: string; color: string }> = {
    pending: { label: "Pending", color: "bg-gray-100 text-gray-800 dark:bg-gray-900/20 dark:text-gray-400" },
    "in-progress": { label: "In Progress", color: "bg-blue-100 text-blue-800 dark:bg-blue-900/20 dark:text-blue-400" },
    resolved: { label: "Resolved", color: "bg-green-100 text-green-800 dark:bg-green-900/20 dark:text-green-400" },
  }
  return badges[status] || badges.pending
}

export const getSeverityColor = (severity: number) => {
  if (severity >= 90) return "text-red-600 bg-red-50 dark:text-red-400 dark:bg-red-950/20"
  if (severity >= 80) return "text-orange-600 bg-orange-50 dark:text-orange-400 dark:bg-orange-950/20"
  if (severity >= 70) return "text-yellow-600 bg-yellow-50 dark:text-yellow-400 dark:bg-yellow-950/20"
  return "text-green-600 bg-green-50 dark:text-green-400 dark:bg-green-950/20"
}

